type coinbaseProofPool interface {
	AddCoinbaseProof(mining.CoinbaseProof) (chainhash.Hash, error)
}

func (s *rpcServer) coinbaseProofContext() (uint32, int64) {
	best := s.cfg.Chain.BestSnapshot()
	return uint32(best.Height + 1), best.Timestamp.Unix()
}

func (s *rpcServer) addRawCoinbaseProof(
	raw blockchain.RawCoinbaseProof) error {

	mp, ok := s.cfg.TxMemPool.(coinbaseProofPool)
	if !ok {
		return fmt.Errorf("coinbase proof mempool is unavailable")
	}
	if _, err := mp.AddCoinbaseProof(coinbaseProofForMining(raw)); err != nil {
		return err
	}

	if s.cfg.ConnMgr != nil {
		invType := wire.InvTypeAirDrop
		if raw.Output != nil && raw.Output.Covenant.Type == wire.CovenantClaim {
			invType = wire.InvTypeClaim
		}
		iv := wire.NewInvVect(invType, &raw.Hash)
		s.cfg.ConnMgr.RelayInventory(iv, nil)
	}
	if s.gbtWorkState != nil {
		s.gbtWorkState.NotifyMempoolTx(s.cfg.TxMemPool.LastUpdated())
	}
	return nil
}

func (s *rpcServer) acceptRawClaimProof(serialized []byte) (
	chainhash.Hash, error) {

	hash := blockchain.RawProofHash(serialized)
	height, prevTime := s.coinbaseProofContext()
	proof, err := blockchain.CoinbaseClaimProofFromRaw(serialized, height,
		prevTime, s.cfg.ChainParams)
	if err != nil {
		return hash, err
	}
	if err := s.addRawCoinbaseProof(proof); err != nil {
		return hash, err
	}
	return proof.Hash, nil
}

func (s *rpcServer) acceptRawAirdropProof(serialized []byte) (
	chainhash.Hash, error) {

	hash := blockchain.RawProofHash(serialized)
	height, _ := s.coinbaseProofContext()
	active, err := s.cfg.Chain.IsDeploymentActive(chaincfg.DeploymentAirstop)
	if err != nil {
		return hash, err
	}
	if active {
		return hash, fmt.Errorf("airdrop proofs are disabled")
	}

	proof, err := blockchain.CoinbaseAirdropProofFromRaw(serialized, height,
		s.cfg.ChainParams)
	if err != nil {
		return hash, err
	}
	if err := s.addRawCoinbaseProof(proof); err != nil {
		return hash, err
	}
	return proof.Hash, nil
}

// handleSetGenerate implements the setgenerate command.
func handleSetGenerate(s *rpcServer, cmd interface{}, closeChan <-chan struct{}) (interface{}, error) {
	c := cmd.(*hnsjson.SetGenerateCmd)

	// Disable generation regardless of the provided generate flag if the
	// maximum number of threads (goroutines for our purposes) is 0.
	// Otherwise enable or disable it depending on the provided flag.
	generate := c.Generate
	genProcLimit := -1
	if c.GenProcLimit != nil {
		genProcLimit = *c.GenProcLimit
	}
	if genProcLimit == 0 {
		generate = false
	}

	if !generate {
		s.cfg.CPUMiner.Stop()
	} else {
		// Respond with an error if there are no addresses to pay the
		// created blocks to.
		if len(cfg.miningAddrs) == 0 {
			return nil, &hnsjson.RPCError{
				Code: hnsjson.ErrRPCInternal.Code,
				Message: "No payment addresses specified " +
					"via --miningaddr",
			}
		}

		// It's safe to call start even if it's already started.
		s.cfg.CPUMiner.SetNumWorkers(int32(genProcLimit))
		s.cfg.CPUMiner.Start()
	}
	return nil, nil
}

// Text used to signify that a signed message follows and to prevent
// inadvertently signing a transaction.
// messageSignatureHeader is prepended to a message before it is hashed and
// signed.  It matches the hsd reference implementation's
// `handshake signed message:\n` magic so signatures round-trip with other
// Handshake implementations.
const messageSignatureHeader = "handshake signed message:\n"

// handleSignMessageWithPrivKey implements the signmessagewithprivkey command.
func handleSignMessageWithPrivKey(s *rpcServer, cmd interface{}, closeChan <-chan struct{}) (interface{}, error) {
	c := cmd.(*hnsjson.SignMessageWithPrivKeyCmd)

	wif, err := hnsutil.DecodeWIF(c.PrivKey)
	if err != nil {
		message := "Invalid private key"
		switch err {
		case hnsutil.ErrMalformedPrivateKey:
			message = "Malformed private key"
		case hnsutil.ErrChecksumMismatch:
			message = "Private key checksum mismatch"
		}
		return nil, &hnsjson.RPCError{
			Code:    hnsjson.ErrRPCInvalidAddressOrKey,
			Message: message,
		}
	}
	if !wif.IsForNet(s.cfg.ChainParams) {
		return nil, &hnsjson.RPCError{
			Code:    hnsjson.ErrRPCInvalidAddressOrKey,
			Message: "Private key for wrong network",
		}
	}

	var buf bytes.Buffer
	wire.WriteVarString(&buf, 0, messageSignatureHeader)
	wire.WriteVarString(&buf, 0, c.Message)
	messageHash := chainhash.DoubleHashB(buf.Bytes())

	sig := ecdsa.SignCompact(wif.PrivKey, messageHash, wif.CompressPubKey)

	return base64.StdEncoding.EncodeToString(sig), nil
}

