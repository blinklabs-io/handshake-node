func gbtCoinbaseProofResults(template *mining.BlockTemplate) (
	[]hnsjson.GetBlockTemplateResultClaim,
	[]hnsjson.GetBlockTemplateResultAirdrop, error) {

	claims := make([]hnsjson.GetBlockTemplateResultClaim, 0)
	airdrops := make([]hnsjson.GetBlockTemplateResultAirdrop, 0)
	for i, proof := range template.CoinbaseProofs {
		if proof.Output == nil {
			return nil, nil, fmt.Errorf("coinbase proof %d missing output", i)
		}

		switch proof.Output.Covenant.Type {
		case wire.CovenantClaim:
			result, err := gbtClaimProofResult(proof, template.Height,
				template.NameDeflationHeight)
			if err != nil {
				return nil, nil, fmt.Errorf("coinbase claim proof %d: %w",
					i, err)
			}
			claims = append(claims, result)

		case wire.CovenantNone:
			result, err := gbtAirdropProofResult(proof)
			if err != nil {
				return nil, nil, fmt.Errorf("coinbase airdrop proof %d: %w",
					i, err)
			}
			airdrops = append(airdrops, result)

		default:
			return nil, nil, fmt.Errorf("coinbase proof %d has "+
				"unsupported covenant type %d", i,
				proof.Output.Covenant.Type)
		}
	}

	return claims, airdrops, nil
}

func gbtClaimProofResult(proof mining.CoinbaseProof, height int32,
	deflationHeight uint32) (hnsjson.GetBlockTemplateResultClaim, error) {

	covenant := proof.Output.Covenant
	if len(covenant.Items) < 6 {
		return hnsjson.GetBlockTemplateResultClaim{},
			fmt.Errorf("CLAIM covenant has %d items", len(covenant.Items))
	}
	if len(covenant.Items[0]) != chainhash.HashSize {
		return hnsjson.GetBlockTemplateResultClaim{},
			fmt.Errorf("CLAIM name hash item has %d bytes",
				len(covenant.Items[0]))
	}
	if len(covenant.Items[3]) == 0 {
		return hnsjson.GetBlockTemplateResultClaim{},
			fmt.Errorf("CLAIM flags item is empty")
	}
	if len(covenant.Items[4]) != chainhash.HashSize {
		return hnsjson.GetBlockTemplateResultClaim{},
			fmt.Errorf("CLAIM commit hash item has %d bytes",
				len(covenant.Items[4]))
	}
	if len(covenant.Items[5]) != 4 {
		return hnsjson.GetBlockTemplateResultClaim{},
			fmt.Errorf("CLAIM commit height item has %d bytes",
				len(covenant.Items[5]))
	}

	value, err := gbtCoinbaseProofValue(proof)
	if err != nil {
		return hnsjson.GetBlockTemplateResultClaim{}, err
	}
	fee := proof.Fee
	commitHeight := binary.LittleEndian.Uint32(covenant.Items[5])
	if height >= 0 && uint32(height) >= deflationHeight &&
		commitHeight != 1 {

		value = proof.Output.Value
		fee = 0
	}

	name := covenant.Items[2]
	address := proof.Output.Address
	return hnsjson.GetBlockTemplateResultClaim{
		Data:         hex.EncodeToString(proof.Witness),
		Name:         string(name),
		NameHash:     hex.EncodeToString(covenant.Items[0]),
		Version:      address.Version,
		Hash:         hex.EncodeToString(address.Hash),
		Value:        value,
		Fee:          fee,
		Weak:         covenant.Items[3][0]&1 != 0,
		CommitHash:   hex.EncodeToString(covenant.Items[4]),
		CommitHeight: commitHeight,
		Weight:       gbtClaimProofWeight(proof, name),
	}, nil
}

func gbtAirdropProofResult(proof mining.CoinbaseProof) (
	hnsjson.GetBlockTemplateResultAirdrop, error) {

	proofData, err := parseGBTAirdropProof(proof.Witness)
	if err != nil {
		return hnsjson.GetBlockTemplateResultAirdrop{}, err
	}
	value, err := gbtCoinbaseProofValue(proof)
	if err != nil {
		return hnsjson.GetBlockTemplateResultAirdrop{}, err
	}

	var rate int64
	if proofData.virtualSize > 0 {
		rate = proof.Fee * 1000 / proofData.virtualSize
	}
	address := proof.Output.Address
	return hnsjson.GetBlockTemplateResultAirdrop{
		Data:     hex.EncodeToString(proof.Witness),
		Position: proofData.position,
		Version:  address.Version,
		Address:  hex.EncodeToString(address.Hash),
		Value:    value,
		Fee:      proof.Fee,
		Rate:     rate,
		Weak:     proofData.weak,
	}, nil
}

func gbtCoinbaseProofValue(proof mining.CoinbaseProof) (int64, error) {
	if proof.Output.Value < 0 {
		return 0, fmt.Errorf("coinbase proof output value is negative")
	}
	if proof.Fee < 0 {
		return 0, fmt.Errorf("coinbase proof fee is negative")
	}
	if proof.Output.Value > hnsutil.MaxDoo-proof.Fee {
		return 0, fmt.Errorf("coinbase proof value exceeds max money")
	}
	return proof.Output.Value + proof.Fee, nil
}

func gbtClaimProofWeight(proof mining.CoinbaseProof, name []byte) int64 {
	size := 1 + 8 + proof.Output.Address.SerializeSize() + 90 + len(name)
	weight := size * blockchain.WitnessScaleFactor
	return int64(1 + wire.VarIntSerializeSize(uint64(len(proof.Witness))) +
		len(proof.Witness) + weight)
}

type gbtAirdropProofData struct {
	position    uint32
	virtualSize int64
	weak        bool
}

func parseGBTAirdropProof(serialized []byte) (gbtAirdropProofData, error) {
	if len(serialized) == 0 {
		return gbtAirdropProofData{}, fmt.Errorf("airdrop proof is empty")
	}

	r := bytes.NewReader(serialized)
	index, err := gbtReadU32(r)
	if err != nil {
		return gbtAirdropProofData{}, err
	}

	proofCount, err := r.ReadByte()
	if err != nil {
		return gbtAirdropProofData{}, err
	}
	if err := gbtSkip(r, int(proofCount)*chainhash.HashSize); err != nil {
		return gbtAirdropProofData{}, err
	}
	if _, err := r.ReadByte(); err != nil {
		return gbtAirdropProofData{}, err
	}
	subProofCount, err := r.ReadByte()
	if err != nil {
		return gbtAirdropProofData{}, err
	}
	if err := gbtSkip(r, int(subProofCount)*chainhash.HashSize); err != nil {
		return gbtAirdropProofData{}, err
	}

	key, err := gbtReadVarBytes(r)
	if err != nil {
		return gbtAirdropProofData{}, err
	}
	if _, err := r.ReadByte(); err != nil {
		return gbtAirdropProofData{}, err
	}
	addressSize, err := r.ReadByte()
	if err != nil {
		return gbtAirdropProofData{}, err
	}
	if err := gbtSkip(r, int(addressSize)); err != nil {
		return gbtAirdropProofData{}, err
	}
	if _, err := wire.ReadVarInt(r, 0); err != nil {
		return gbtAirdropProofData{}, err
	}
	if _, err := gbtReadVarBytes(r); err != nil {
		return gbtAirdropProofData{}, err
	}
	if r.Len() != 0 {
		return gbtAirdropProofData{}, fmt.Errorf("trailing airdrop proof data")
	}

	position, err := gbtAirdropPosition(index, key)
	if err != nil {
		return gbtAirdropProofData{}, err
	}
	return gbtAirdropProofData{
		position: position,
		virtualSize: int64((len(serialized) +
			blockchain.WitnessScaleFactor - 1) /
			blockchain.WitnessScaleFactor),
		weak: gbtAirdropKeyWeak(key),
	}, nil
}

func gbtAirdropPosition(index uint32, key []byte) (uint32, error) {
	if len(key) > 0 && key[0] == gbtAirdropKeyAddress {
		if index >= gbtFaucetLeaves {
			return 0, fmt.Errorf("faucet airdrop index %d is out of range",
				index)
		}
		return index + gbtAirdropLeaves, nil
	}
	if index >= gbtAirdropLeaves {
		return 0, fmt.Errorf("airdrop index %d is out of range", index)
	}
	return index, nil
}

func gbtAirdropKeyWeak(key []byte) bool {
	if len(key) < 3 || key[0] != gbtAirdropKeyRSA {
		return false
	}

	modulusSize := int(binary.LittleEndian.Uint16(key[1:3]))
	if len(key) < 3+modulusSize {
		return false
	}
	return new(big.Int).SetBytes(key[3:3+modulusSize]).BitLen() <
		gbtStrongRSAKeyBits
}

func gbtReadU32(r *bytes.Reader) (uint32, error) {
	var raw [4]byte
	if _, err := io.ReadFull(r, raw[:]); err != nil {
		return 0, err
	}
	return binary.LittleEndian.Uint32(raw[:]), nil
}

func gbtReadVarBytes(r *bytes.Reader) ([]byte, error) {
	size, err := wire.ReadVarInt(r, 0)
	if err != nil {
		return nil, err
	}
	if size > uint64(r.Len()) {
		return nil, fmt.Errorf("varbytes length %d exceeds remaining %d",
			size, r.Len())
	}
	out := make([]byte, size)
	if _, err := io.ReadFull(r, out); err != nil {
		return nil, err
	}
	return out, nil
}

func gbtSkip(r *bytes.Reader, n int) error {
	if n < 0 || n > r.Len() {
		return fmt.Errorf("skip length %d exceeds remaining %d", n, r.Len())
	}
	_, err := r.Seek(int64(n), io.SeekCurrent)
	return err
}

